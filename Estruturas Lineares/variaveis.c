#include <stdio.h>

int main()
{
    /*
        tipos de variaveis
    */

    int inteiro = 5;
    float flutuante = 3.56;
    double duplo = 5.89;
    char caracter = 's';
    
    printf("a variavel inteiro vale %d\n", inteiro);
    printf("a variavel flutuante vale %.2f\n", flutuante);
    printf("a variavel double vale %.2f\n", duplo);
    printf("a variavel caracter vale %c\n", caracter);

    return 0;
}
